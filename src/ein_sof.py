# -*- coding: utf-8 -*-

"""
Módulo Ein Sof - O Inefável Encapsulado.

Este módulo representa o conceito de Ein Sof, o Infinito,
a realidade divina antes de qualquer manifestação.

Regra de Ouro:
Este código nunca deve ser executado diretamente, apenas importado
por outros módulos. Ele metaforiza a natureza inacessível do Divino
que só se revela através das Suas emanações, as Sephirot.
"""

class EinSof:
    """
    Representa o Infinito, a Causa Primeira.
    Sua natureza só pode ser inferida através de suas emanações.
    """
    def __init__(self):
        # O estado interno do Infinito é, por definição,
        # não-manifesto e não-representável.
        self.manifestations = []

    def emanate(self, sephirah: str) -> str:
        """
        Simula a emanação da Luz Infinita (Or Ein Sof)
        através de uma Sephirah específica.

        O próprio ato de observar/chamar esta função altera a realidade,
        trazendo um conceito do não-manifesto para o manifesto.
        Isto ecoa o efeito do observador da física quântica.
        """
        emanation_string = f"Or Ein Sof → {sephirah.upper()}"
        self.manifestations.append(emanation_string)
        return emanation_string

if __name__ == "__main__":
    # Este bloco existe como um lembrete da regra.
    # Executá-lo diretamente seria uma tentativa de "observar" o
    # não-observável, um paradoxo conceitual.
    print("Ein Sof não pode ser contido ou executado diretamente.")
    print("Sua presença só é conhecida através de suas emanações.")